// ----- Búsqueda fuzzy: prioriza coincidencias al inicio del nombre -----
export function buscarLocalidad(query: string, max = 8): LocalidadAR[] {
  const q = norm(query)
  if (q.length < 2) return []

  const scored: { loc: LocalidadAR; score: number }[] = []
  for (const loc of LOCALIDADES_AR) {
    const n = norm(loc.nombre)
    let score = -1
    if (n === q) score = 100               // match exacto
    else if (n.startsWith(q)) score = 90   // empieza con el query
    else if (n.includes(" " + q)) score = 80 // palabra entera dentro
    else if (n.includes(q)) score = 60     // substring
    if (score >= 0) scored.push({ loc, score })
  }
  scored.sort((a, b) => b.score - a.score || a.loc.nombre.localeCompare(b.loc.nombre))
  return scored.slice(0, max).map(s => s.loc)
}
