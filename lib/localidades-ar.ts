/**
 * Localidades de Argentina con su Código Postal central.
 * Cobertura prioritaria: CABA + AMBA (~80% de pólizas de un broker) +
 * capitales de provincia + ciudades grandes.
 *
 * Para CABA, el CP es por barrio. Para localidades chicas, el CP es único.
 * Para ciudades grandes con múltiples CPs, devolvemos el CP central
 * (el usuario puede ajustarlo manualmente si tiene el detalle por calle).
 *
 * Fuente base: Correo Argentino + dataset público AMBA.
 */

export interface LocalidadAR {
  /** Nombre canónico de la localidad (Title Case) */
  nombre: string
  /** Código Postal de 4 dígitos */
  cp: string
  /** Provincia o "CABA" */
  provincia: string
}

// ----- Helper de normalización para búsqueda (sin tildes ni mayúsculas) -----
function norm(s: string): string {
  return s
    .normalize("NFD")
    .replace(/[̀-ͯ]/g, "")
    .toLowerCase()
    .trim()
}

